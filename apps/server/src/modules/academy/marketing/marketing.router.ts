router.delete('/admin/marketing/banners/:id', academyAuthenticate, academyRequireAdmin, controller.deleteBanner);
router.post('/admin/marketing/promotions', academyAuthenticate, academyRequireAdmin, controller.createPromotion);
router.patch('/admin/marketing/promotions/:id', academyAuthenticate, academyRequireAdmin, controller.updatePromotion);
router.delete('/admin/marketing/promotions/:id', academyAuthenticate, academyRequireAdmin, controller.deletePromotion);
router.post('/admin/marketing/codes', academyAuthenticate, academyRequireAdmin, controller.createCode);
router.patch('/admin/marketing/codes/:id', academyAuthenticate, academyRequireAdmin, controller.updateCode);
router.delete('/admin/marketing/codes/:id', academyAuthenticate, academyRequireAdmin, controller.deleteCode);
