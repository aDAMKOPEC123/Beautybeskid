import { Router } from 'express';
import { academyAuthenticate, academyRequireAdmin } from '../../../middleware/academy-auth.middleware';
import * as controller from './marketing.controller';
import { upload } from '../../../config/multer';
const router = Router();
router.get('/public/storefront', controller.storefront);
router.post('/public/banners/:id/event', controller.bannerEvent);
router.post('/public/leads', controller.lead);
router.post('/public/leads/unsubscribe/:token', controller.unsubscribe);
router.get('/public/email/open/:token', controller.emailOpen);
router.get('/public/email/click/:token', controller.emailClick);
router.post('/marketing/preview-code', academyAuthenticate, controller.previewCode);
router.get('/admin/marketing', academyAuthenticate, academyRequireAdmin, controller.adminList);
router.post('/admin/marketing/send-campaign', academyAuthenticate, academyRequireAdmin, controller.sendCampaign);
router.get('/admin/marketing/campaigns', academyAuthenticate, academyRequireAdmin, controller.campaigns);
